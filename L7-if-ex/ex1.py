num = int(input("Tell me the day of the week. (0 to 6): "))
if num == 0:
    print("Sunday")
elif num == 1:
    print("Monday")
elif num == 2:
    print("Tuesday")
elif num == 3:
    print("Wednesday")
elif num == 4:
    print("Thursday")
elif num == 5:
    print("Friday")
elif num == 6:
    print("Saturday")
else:
    print("Please enter a number between 0 and 6!")
